var x1 = prompt('Введите координату x для первой точки');
var y1 = prompt('Введите координату y для первой точки');

var x2 = prompt('Введите координату x для второй точки');
var y2 = prompt('Введите координату y для второй точки');

var x = 'x';

var k = (y1 - y2) / (x1 - x2);
var b = y2 - k*x2;

var y = k + x + '+' + b;

alert('Результат равен ' + y);